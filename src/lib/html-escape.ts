/**
 * HTML エスケープの共有ヘルパー。
 *
 * メール本文 (招待 / マジックリンク / チケット返信) を組み立てるとき、外部由来の文字列
 * (URL・氏名・コメント本文など) を HTML に差し込む前に最低限のエスケープを行うための純粋関数。
 * 同じ処理が複数のメールテンプレートで必要になるため 1 か所に集約する (DRY)。
 */

// HTML に挿入する文字列を最低限エスケープする (危険な 5 文字だけを実体参照に変換)
export function escapeHtml(s: string): string {
  // & を最初に置換する (後続の置換で生成される実体参照を二重エスケープしないため)
  return s
    .replace(/&/g, '&amp;') // アンパサンドを実体参照へ
    .replace(/</g, '&lt;') // タグ開始記号を実体参照へ
    .replace(/>/g, '&gt;') // タグ終了記号を実体参照へ
    .replace(/"/g, '&quot;') // 二重引用符を実体参照へ (属性値の途中切断を防ぐ)
    .replace(/'/g, '&#39;'); // 単一引用符を実体参照へ
}
