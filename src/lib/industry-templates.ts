// ID でテンプレートを 1 件取得するヘルパー関数 (見つからなければ undefined を返す)
export function findIndustryTemplate(id: string): IndustryTemplate | undefined {
  // 配列を線形探索して id が一致するものを返す
  return INDUSTRY_TEMPLATES.find((t) => t.id === id);
}
